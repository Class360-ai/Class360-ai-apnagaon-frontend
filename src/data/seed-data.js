export const getSeedProducts = () => seedProducts;
export const getSeedServices = () => seedServices;

export const getProductsByCategory = (categorySlug) => {
  return seedProducts.filter(p => p.category === categorySlug);
};

export const getProductsBySubcategory = (subcategorySlug) => {
  return seedProducts.filter(p => p.subcategory === subcategorySlug);
};

export const getServicesByCategory = (categorySlug) => {
  return seedServices.filter(s => s.category === categorySlug);
};

export const getSeasonalOffers = () => seedOffers;

export const getOffersByCategory = (categorySlug) => {
  return seedOffers.filter((offer) =>
    Array.isArray(offer.categories) && offer.categories.includes(categorySlug)
  );
};

export const getProductById = (id) => {
  return seedProducts.find(p => p.id === id);
};

export const getServiceById = (id) => {
  return seedServices.find(s => s.id === id);
};

