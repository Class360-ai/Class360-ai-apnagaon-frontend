const applyWordTranslation = (text) => {
  return text
    .split(" ")
    .map((part) => {
      const clean = part.replace(/[^\w&/.-]/g, "");
      const translated = wordHindiMap[clean];
      if (!translated) return part;
      return part.replace(clean, translated);
    })
    .join(" ");
};

export const translateDynamicText = (value, lang = "en") => {
  if (value === null || value === undefined) return "";
  if (typeof value === "object") {
    if (typeof value[lang] === "string") return value[lang];
    if (typeof value.en === "string") return value.en;
    return "";
  }
  if (typeof value !== "string") return String(value);
  if (lang !== "hi") return value;

  const direct = dynamicHindiText[value];
  if (direct) return direct;

  const wordBased = applyWordTranslation(value);
  if (wordBased !== value) return wordBased;

  return value;
};

const localizeNode = (node, lang) => {
  if (Array.isArray(node)) {
    return node.map((item) => localizeNode(item, lang));
  }
  if (!node || typeof node !== "object") {
    return node;
  }

  const localized = {};
  for (const [key, value] of Object.entries(node)) {
    if (typeof value === "string" && ["name", "title", "subtitle", "badge", "actionLabel", "label", "text"].includes(key)) {
      localized[key] = translateDynamicText(value, lang);
    } else {
      localized[key] = localizeNode(value, lang);
    }
  }
  return localized;
};

export const localizeDataTree = (data, lang = "en") => localizeNode(data, lang);
